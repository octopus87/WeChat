package com.zy.wechat.dataVO;

public class ImageVO {
	private String media_id;
	
	public ImageVO() {
		super();
	}

	public ImageVO(String media_id) {
		super();
		this.media_id = media_id;
	}

	public String getMedia_id() {
		return media_id;
	}

	public void setMedia_id(String media_id) {
		this.media_id = media_id;
	}
	
}
