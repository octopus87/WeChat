package com.zy.wechat.dataVO;

public class TextVO {
	private String content;

	
	public TextVO() {
		super();
	}

	public TextVO(String content) {
		super();
		this.content = content;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}
	
	
	
}
